let lines = fs.readFileSync('input.txt', 'utf8')
    .split('\n')
    .map(line => line.trim())
    .filter(line => line.length > 0)

let map = Map(lines)

let sum = 0
for (let y = 0; y < map.height; ++y) {
    if (map.tree(3 * y, y)) {
        ++sum
    }
}

console.log(sum)
